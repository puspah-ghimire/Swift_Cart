class Product {
  constructor(name, price, description, category, brand, amountInStock, rating, numOfReviews, images) {
    this.name = name;
    this.price = price;
    this.description = description;
    this.category = category;
    this.brand = brand;
    this.amountInStock = amountInStock;
    this.rating = rating;
    this.numOfReviews = numOfReviews;
    this.images = images;
  }
}

export default Product;


